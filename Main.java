class Main{
    public static void main(String[]args){
        System.out.println("Hello World");
        System.out.println(5+3);

        // データ型　10をnumberに代入
        int number = 10;
        System.out.println(number+3);
        
        // numberの上書き / データ型を書かない 
        number = 2;
        System.out.println(number+3);

        String name;
        name = "Nyan";
        System.out.println("名前は"+name+"です");
    }
}

